#include <unistd.h>

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 47;
	while (a < 57)
	{
		b = 47;
		a++;
		while (b < 57)
		{
			b++;
			write(1, &a, 1);
			write(1, &b, 1);
			if (!(a == 57 && b == 57))
				write(1, ", ", 1);
		}
	}
}
