int	ft_atoi(char *str)
{
	int	i;
	int	nb;
	int	sign;
	int	j;

	i = 0;
	j = 0;
	nb = 0;
	sign = 1;
	while (str[i] <= 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i + j] >= '0' && str[i + j] <= '9')
	{
		nb *= 10;
		nb += str[i + j] - '0';
		j++;
	}
	nb *= sign;
	return (nb);
}
